    public void TableAuteur() {
    String[] livre = {"ID", "NÂ°ISBN", "TITRE", "AUTEUR"};
    String[] afficher = new String[4];  // Changed size to 4 to match columns
    DefaultTableModel model = new DefaultTableModel(null, livre);
    String sql = "select * from Livre";
    
    try {
        // Load SQLite JDBC driver and establish connection
        Class.forName("org.sqlite.JDBC");
        con = DriverManager.getConnection("jdbc:sqlite:gestionbib.db");
        
        // Create a Statement object and execute the query
        Statement st = con.createStatement();
        rs = st.executeQuery(sql);  // Corrected to use 'st.executeQuery(sql)'
        
        // Iterate through the result set and populate the table
        while (rs.next()) {
            afficher[0] = rs.getString("id_livre");  // Column name must match database
            afficher[1] = rs.getString("nisbn");
            afficher[2] = rs.getString("titre");
            afficher[3] = rs.getString("auteur");
            model.addRow(afficher);
        }
        
        // Set the table model to update the UI
        TableAuteur.setModel(model);
        
        // Close the connection
        con.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
}
